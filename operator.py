#!/usr/bin/env python3

# Assignment: =
base = 12
print("base = 12 : "+str(base))

# Arithmetic: + - * / % // **
print("base+base : "+str(base+base))
print("base-base : "+str(base-base))
print("base*base : "+str(base*base))
print("base/base : "+str(base/base))
print("base%%base : "+str(base%base))
print("base//base : "+str(base//base))
print("base**base : "+str(base**base))

# Comparison: == != >= > <= <
print("base==12 : "+str(base==12))
print("base!=12 : "+str(base!=12))
print("base>=12 : "+str(base>=12))
print("base>12 : "+str(base>12))
print("base<=12 : "+str(base<=12))
print("base<12 : "+str(base<12))

# Logical: and or not
print("True and False : "+str(True and False))
print("True or False : "+str(True or False))
print("not True : "+str(not True))